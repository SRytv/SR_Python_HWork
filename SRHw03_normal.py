# Напишите функцию, возвращающую ряд Фибоначчи с n-элемента до m-элемента.
# Первыми элементами ряда считать цифры 1 1

#=====================================================

def fibonacci(n, m):
    fibonacci_serie = []
    def fib(n):
            if n<2:
                return n
            curr  = fib(n-1) + fib(n-2)
            return curr
    for i in range(n, m):
        fibonacci_serie.append(fib(i))
    return fibonacci_serie

