def sort_to_max(origin_list):
    new_list = origin_list[:]
    for i in range(1,(len(new_list)-1)):
        for j in range((len(new_list)-i)):
            if int(new_list[j]) > int(new_list[j+1]):
                (new_list[j]), (new_list[j+1]) = (new_list[j+1]), (new_list[j])
    return new_list

